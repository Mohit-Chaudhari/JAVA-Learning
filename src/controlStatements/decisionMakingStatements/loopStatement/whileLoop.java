package controlStatements.decisionMakingStatements.loopStatement;

public class whileLoop {
    // Calculation program
    public static void main(String[] args) {
        int i = 0;
        System.out.println("Printing the even numbers upto 10");
        while(i <= 10){
            System.out.println(i);
            i += 2;
        }
    }
}
